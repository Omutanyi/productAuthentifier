function showOptions(){
    document.getElementById("optionsId").style.display = 'block';
}
function closeOptions(){
    document.getElementById("optionsId").style.display = 'none';
}
function showProductOptions(){
    document.getElementById("optionsId").style.display = 'none';
    document.getElementById("productOptionsId").style.display = 'block';
}
function closeProductOptions(){
    document.getElementById("productOptionsId").style.display = 'none';
}
function showBatchOptions(){
    document.getElementById("optionsId").style.display = 'none';
    document.getElementById("batchOptionsId").style.display = 'block';
}
function closeBatchOptions(){
    document.getElementById("batchOptionsId").style.display = 'none';
}
